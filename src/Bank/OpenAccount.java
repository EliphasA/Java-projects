    private void btnAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccountActionPerformed
        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please create an account first!");
        } else {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                String url = "jdbc:sqlserver://localhost:1433; databaseName=Bank; user=sa; password=Nust2019";
                Connection con = (Connection) DriverManager.getConnection(url);

                String ver = "select AccountNo from Account where IDNumber=?";
                PreparedStatement pre = con.prepareStatement(ver);

                pre.setString(1, txtID.getText());
                ResultSet re = pre.executeQuery();

                if (re.next()) {
                    String accountNo = re.getString("AccountNo");
                    txtAccountNo.setText(accountNo);
                }else{
                JOptionPane.showMessageDialog(null, "Please open an account first!");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_btnAccountActionPerformed


