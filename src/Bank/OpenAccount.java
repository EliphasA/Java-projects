    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String pin = new String(this.pwdPin.getPassword());

        if (txtFName.getText().isEmpty() || txtLName.getText().isEmpty() || txtID.getText().isEmpty()
                || pin.isEmpty() || txtDeposit.getText().isEmpty() || txtAccount.getText().isEmpty()) {

            if (txtFName.getText().isEmpty()) {
                txtFName.setBorder(new LineBorder(Color.RED));
            }
            if (txtLName.getText().isEmpty()) {
                txtLName.setBorder(new LineBorder(Color.RED));
            }
            if (txtID.getText().isEmpty()) {
                txtID.setBorder(new LineBorder(Color.RED));
            }
            if (pin.isEmpty()) {
                pwdPin.setBorder(new LineBorder(Color.RED));
            }
            if (txtDeposit.getText().isEmpty()) {
                txtDeposit.setBorder(new LineBorder(Color.RED));
            }
            if (txtAccount.getText().isEmpty()) {
                txtAccount.setBorder(new LineBorder(Color.RED));
            }

            JOptionPane.showMessageDialog(null, "Please enter all fields!");

        } else {
            try {
                //Database connection
                Class.forName("com.mysql.cj.jdbc.Driver");
                String url = "jdbc:sqlserver://localhost:1433; databaseName=Bank; user=sa; password=Nust2019";
                Connection con = (Connection) DriverManager.getConnection(url);
                Statement st = con.createStatement();

                CallableStatement cal = con.prepareCall("{call CreateAccount(?,?,?,?,?,?,?)}"); //calling the procedure
                cal.setString(1, txtFName.getText());
                cal.setString(2, txtLName.getText());
                cal.setString(3, txtID.getText());
                cal.setString(4, (String) cboxGender.getSelectedItem());
                cal.setString(5, pin);
                cal.setString(6, txtDeposit.getText());
                cal.setString(7, txtAccount.getText());
                cal.execute();

                JOptionPane.showMessageDialog(null, "Account successfully created!");

                Menu menu = new Menu();
                FlowLayout l = new FlowLayout();

                this.removeAll();
                this.revalidate();
                this.repaint();

                this.add(menu);
                this.setLayout(l);
                this.revalidate();
                this.repaint();
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "There is an account with the same ID number, please make sure you are puting in a correct ID number!");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }

        }
    }//GEN-LAST:event_btnCreateActionPerformed

